            <p>Puma IFV <br>

Der PUMA IFV ist ein deutscher Schützenpanzer (englisch: IFV) und befindet sich momentan im Dienst mit der Bundeswehr, spezifisch den Einheiten der Panzergrenadiere. Auch wenn der ungefähre Aufbau auf dem veralteten Marder IFV basiert, so handelt es sich beim PUMA um ein eigenständiges Fahrzeug, welches eine drastische Kampfwertsteigerung erfahren hat.
Bei einem IFV kommt es vor allem auf den Schutz und die Unterstützung der an Bord getragenen Soldaten an. Dabei glänzt der PUMA auf. Auf beiden Seiten des Fahrzeugs finden sich fortschrittliche ERA-Blöcke, welche bei Beschuss explodieren, um Großkaliber-Geschosse und chemische Penetratoren abzuwehren. Dahinter und auch darunter befinden sich dicke Platten aus Komposit-Material, welche zusätzlichen Schuss gegen kinetischen Beschuss bieten.
Ebenfalls revolutionär im Besatzungsschutz ist das MUSS - ein aktives Verteidigungssystem. Es umfasst Laser-Warnsysteme, automatische Nebelwerfer und einen Infrarot-Störsender, welcher die Lenkkomponente von Panzerabwehrraketen stört und die Waffen umlenkt.
Zur Unterstützung der Truppen hat der PUMA die MK30-2/ABM. Es handelt sich dabei um eine vollautomatisch, ferngesteuerte 30mm Waffenanlage mit 400 Schuss automatischem Feed. Außerdem montiert der Turm die koaxiale 5.56 mm MG4 von Heckler und Koch, welche eine Reserve von 2000 Schuss umfasst.
In der Kampfwertgesteigerten Variante MELLS trägt das IFV das gleichnamige Waffensystem an den Turm gehangen und feuert die Lizenzproduzierten EuroSpike LR- Panzerabwehrraketen ab. Dabei handelt es sich um einen der besten Lenkflugkörper in seinem Feld.
Das Feuerleitsystem ist ebenfalls eines der besten in der Welt. Mit programmierter Munition ist der Puma in der Lage, Infanteristen hinter Deckung, Kampfdrohnen und sogar Flugzeuge effektiv zu bekämpfen. Dies bietet Panzergrenadieren auch ohne MANPADS die Möglichkeit, Kampfverbände vor Luftangriffen zu schützen. Da das Feuerleitsystem auf passiven Signalen beruht, ist der Puma außerdem sicher vor ELINT-Angriffen.
Aufgrund seines umfangreichen Besatzungsschutzes und der unverhältnismäßig starken Bewaffnung gilt der Puma als das modernste und beste IFV der Welt.</p>
